let num1 = 4;
let num2 = 6;
let result 
let operactor = prompt("Choose the operaactor");

switch (operactor) {
    case "+":
        result = num1 + num2;
        alert("Result: " + result);
        break;

    case "-":
        result = num1 - num2;
        alert("Result: " + result);
        break;

    case "*":
        result = num1 * num2;
        alert("Result: " + result);
        break;

    case "/":
        result = num1 / num2;
        alert("Result: " + result);
        break;

    default:
        break;
}





